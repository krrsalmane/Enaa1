public class Instructor {

    private String Instructor_Specialization;
    private double salary;

    public String getInstructor_Specialization() {
        return Instructor_Specialization;
    }

    public void setInstructor_Specialization(String instructor_Specialization) {
        Instructor_Specialization = instructor_Specialization;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }
}
